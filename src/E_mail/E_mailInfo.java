    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            Myconnect();
            
            String email=txtmail.getText().toString();
            String pas=txtpass.getText().toString();
            if(email.isEmpty()){
                JOptionPane.showMessageDialog(this, "Please!enter email ID");
            }
            else if(pas.isEmpty()){
                JOptionPane.showMessageDialog(this, "Please!enter password");
            }
            else{
            String m="INSERT INTO `e_mail` VALUES ('"+email+"','"+pas+"')";
            int a=pst.executeUpdate(m);
            if(a>0){
                new E_mail1().setVisible(true);
                this.dispose();
            }
            }
        } catch (SQLException ex) {
            Logger.getLogger(E_mailInfo.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

