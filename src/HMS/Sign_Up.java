    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            Myconnect();
            String name=txtname.getText().toString();
            String email=txtpass.getText().toString();
            String com=txtcompass.getText().toString();
            String com1=txtcompass1.getText().toString();
            if(com1.isEmpty()){
                JOptionPane.showMessageDialog(this,"Please! Give comfirm password");
                txtcompass1.requestFocus();
            }
          if(com.contains(com1)){
              
            
            String m="UPDATE `log_in` SET `password`='"+com+"'  WHERE `name`=? AND `password`=?";
            pst=con.prepareStatement(m);
            
            pst.setString(2, email);
            pst.setString(1, name);
            
            int a=pst.executeUpdate();
            if(a>0){
                
                JOptionPane.showMessageDialog(this, "Password Change succesful!");
                txtname.setText("");
                
                txtpass.setText("");
                txtcompass.setText("");
                txtcompass1.setText("");
            }
            else{
                JOptionPane.showMessageDialog(this, "Cannot Change!");
            
            }
          }
          else{
               JOptionPane.showMessageDialog(this,"Sorry! your Comfirm password does not matched");
          }
        } catch (SQLException ex) {
            Logger.getLogger(Sign_Up.class.getName()).log(Level.SEVERE, null, ex);
        }
        catch(NullPointerException ex){
            JOptionPane.showMessageDialog(this, "Please! Fill up your form");
        }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

